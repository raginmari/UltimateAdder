import Foundation

open class Adder {
    
    /// Adds the given two numbers.
    ///
    /// - Parameter num1: The first number.
    /// - Parameter num2: The second number.
    /// - Returns: The sum of the given numbers.
    public func add(_ num1: Int, _ num2: Int) -> Int {
        
        return num1 + num2
    }
}
